print("These are all operations in Python")

# Take inputs from the user and convert them to integers or floats
a = float(input("Enter First Number = "))
b = float(input("Enter Second Number = "))

# Perform operations
sum_result = a + b
print("The sum of these two are:", sum_result)

div = a / b
print("The division:", div)

multiplication = a * b
print("The multiplication:", multiplication)

sqrt = a ** b
print("The power (a raised to b):", sqrt)

Modulo = a % b
print("The remainder:", Modulo)

floor_Division = a // b
print("Floor Division:", floor_Division)
